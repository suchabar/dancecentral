            <h3>KEY MOVEMENTS</h3>
            <ul>
                <li><b>Base: </b> The base is the main movement of the Free Step. He crosses his legs forward, backward or
                    sideways. Normally when you use the Base, the dancer tends to move in the direction in which the
                    step is executed. With training develops the ability to make a base on the spot where it is. With
                    more training still manages to make a base in any direction you want, but few achieve this feat.</li>
                <li><b>Hand moves: </b> The hand movements complement the steps with his legs, are usually inspired Tecktonik
                    style.
                </li>
                <li><b>Kicks: </b>The kicks are used very often to increase base.</li>
                <li><b>Spins: </b>The spins (spins) are also widely used and in different ways, tend to have a more attractive
                    visual effect but requires more experience of the dancer to use them.</li>
                <li><b>Combos: </b>Sequence of predefined free movements such as hand moves, kicks and spins, and may even
                    enter footsteps of other dances</li>
            </ul>


