            <h3>ABOUT</h3>
            Free Step is a dance that consists of sliding on the floor movements with their legs and hands under the beats of electronic
            music. The dance is based on elaborate and quick movements, and to include other dance styles such as Melbourne
            Shuffle, Jumpstyle and C-Walk.
            <h3>HISTORY</h3>
            <p>
                The Free Step is the evolution of ancient Rebolation , which is the evolution of another foreign dance called Charleston
                Dance (dance that move the legs, listening to a kind of blues as music), the Brazilian saw, he liked
                and adapted to electronic music creating rebolation. The dance turned fun between young and was very
                successful in Rave parties in Brazil by 2008 and 2009, following the Dance genre was based on the Psy
                Trance and rhythms. In late 2009, it saw the need to create new steps for the dance, as the Rebolation
                was very limited steps forward and back, there was not much variation. The Rebolation then underwent
                a makeover, making the most elaborate steps mainly by the use of hops in their own base. Also in late
                2009, the band called Axe Music Parangolé created a song called Rebolation, inspired by dance, but implying
                that it was based on rebolados of ax and not the movement of the legs. The music was very successful
                in Brazil and with it the general association of the population was made between the Parangolé and Rebolation,
                constraining who said that danced Rebolation. The name "Free Step" was given by "dancers" of this type,
                and was very successful, especially the Internet. Today there are competitions called "Meet Up" in which
