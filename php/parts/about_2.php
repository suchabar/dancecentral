        <h3>ABOUT</h3>
        <p>
            Jumpstyle is an electronic dance style and music genre popular in Eastern Europe, as well as certain parts of Australia and the United States.
            Jumpstyling is often referred to as "Jumpen": a combination of the English word 'Jump' and the Dutch & German suffix '-en' (meaning "to jump" or "jumping").
            It originated in 1997 in Belgium but gathered bigger popularity in their neighboring country the Netherlands in the 2000s.</p>
        </p>
        <h3>HISTORY</h3>
        <p>
            Jumpstyle, originally known simply as jump, was created in Belgium and started in 1997. It was a short-lived small genre
            that didn't gain popularity in its original form. However, it came back to the public during the turn of the century and
            fandom began increasing throughout Europe afterundergoing significant changes in Germany in early 2003.
            After acquiring its current name, jumpstyle was reintroduced in Europe and in 2005 saw artists and groups producing and releasing its music.
            The first key stage of its popularity came between 2007 and 2008 due to the success of music videos such as Scooter's 'The Question Is What Is The Question' and 'Jumping all over the World' which led to their 13th studio album reaching #1 in the UK charts.
            However, there are still some radio stations left, broadcasting Jump in its traditional form. The oldest Jump radio was founded in 2005 and still exists today under the name of JumpStation.FM.
            The style has also been fused with other genres, for example, Major Lazer & The Partysquad mixed a unique track with a jungle vocal sample in the track "Original Don", with Jumpstyle/Hardstyle influences. Also, Joel Fletcher's remix of Savage's 2005 single "Swing" which uses Jumpstyle influences.
        </p>
        
        
